	<h1>Tentang Web</h1>

	<h2>Sebuah Rumah Baru untuk Kisah Klasik</h2>
	<p>
		Selamat datang di sebuah ruang baca digital yang kami dedikasikan untuk menghadirkan salah satu
		karya sastra klasik dunia ke dalam bahasa ibu kita. Website ini berisi terjemahan novel
		legendaris Around the World in Eighty Days karya Jules Verne, yang kami alihbahasakan menjadi
		Keliling Dunia dalam Delapan Puluh Hari.
	</p>
	<p>
		Kisah ini awalnya dipublikasikan pada tahun 1873, mengisahkan petualangan luar biasa seorang
		pria Inggris bernama Phileas Fogg yang berusaha membuktikan bahwa ia bisa mengelilingi dunia
		hanya dalam 80 hari. Perjalanan yang penuh kejutan ini membawa pembaca menjelajahi daratan,
		samudra, dan berbagai budaya—sebuah pengalaman lintas batas waktu dan tempat.
	</p>
	<p>
		Website ini lahir dari rasa kagum kami terhadap karya tersebut dan keinginan untuk memperluas
		akses pembacaan bagi masyarakat Indonesia. Kami ingin memberikan terjemahan yang tidak hanya
		setia pada teks aslinya, tetapi juga mudah dicerna oleh pembaca masa kini. Semua ini
		terinspirasi oleh <a href="https://www.gutenberg.org" target="_blank">Project Gutenberg</a>,
		sebuah perpustakaan digital yang menyediakan teks bebas hak cipta dari karya-karya sastra dunia.
	</p>

	<h2>Mengapa Kami Membuatnya</h2>
	<p>
		Kami percaya bahwa karya klasik seharusnya tidak berdebu di rak, melainkan terus bernafas dan
		ditemukan kembali oleh generasi baru.
	</p>

	<p>Lewat website ini, kami ingin:</p>

	<ul>
