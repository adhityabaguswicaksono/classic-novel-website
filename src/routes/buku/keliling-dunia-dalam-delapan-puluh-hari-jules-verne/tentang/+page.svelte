		</li>
	</ul>

	<h2>Sekilas Tentang Penulis</h2>
	<p>
		<b>Jules Gabriel Verne</b> (1828–1905) adalah seorang penulis visioner asal Nantes, Prancis, yang
		dikenal luas sebagai salah satu bapak fiksi ilmiah. Ia memiliki kemampuan unik untuk memadukan pengetahuan
		ilmiah pada masanya dengan imajinasi yang melampaui zamannya, menciptakan kisah petualangan yang
		abadi.
	</p>
	<p>
		Karya-karyanya seringkali memprediksi kemajuan teknologi di masa depan—dari kapal selam hingga
		perjalanan luar angkasa—jauh sebelum hal tersebut menjadi kenyataan. Novel-novelnya, seperti
		<b>Twenty Thousand Leagues Under the Seas</b>, <b>Journey to the Center of the Earth</b>, dan
		tentu saja <b>Around the World in Eighty Days</b>, telah menginspirasi pembaca di seluruh dunia
		selama lebih dari satu abad.
	</p>
	<p>
		Dalam Keliling Dunia dalam Delapan Puluh Hari, Verne tidak hanya bercerita tentang kecepatan dan
		pencapaian, tetapi juga tentang tekad, kecerdikan, dan keragaman dunia. Dengan detail geografis
		yang memukau, humor yang halus, dan karakter yang hidup, Verne mengajak kita melakukan
		perjalanan yang terasa nyata, bahkan ketika kita hanya membacanya dari layar atau halaman buku.
	</p>

